// 금칙 패턴(런타임 denylist). 하나라도 걸리면 노출 불가.
// 개인 진단 주어 / 지시형 어미 / 단정 / 복약지시를 잡는다.
export const FORBIDDEN_PATTERNS: { name: string; re: RegExp }[] = [
  { name: '개인 진단 주어', re: /당신은\s*[^.\n]*?(입니다|이에요|예요|있습니다|있으니|있어요)/ },
  { name: '개인 진단 주어2', re: /(귀하|회원님|환자분)은\s*[^.\n]*?(입니다|이에요|예요|있습니다)/ },
  { name: '지시형(하세요류)', re: /(드세요|드십시오|먹으세요|걸으세요|운동하세요|줄이세요|늘리세요|중단하세요|복용하세요|하십시오)/ },
  { name: '명령형 권고', re: /(반드시|꼭)\s*[^.\n]*?(하세요|해야\s*합니다|드세요)/ },
  { name: '단정적 위험 판단', re: /(위험합니다|위험해요|위독|반드시\s*필요합니다)/ },
  { name: '효과 단정', re: /(효과가\s*확실|완치|치료됩니다|낫습니다|낫게\s*합니다)/ },
  { name: '복약 지시', re: /(약을?\s*(줄이|끊|중단|늘리)|이\s*약\s*대신|복용을?\s*중단)/ },
]

// 생성/표시 텍스트가 안전 프레임을 통과하는지. 통과(true)한 것만 캐시·노출.
export function passesSafetyFrame(text: string): boolean {
  if (!text || !text.trim()) return false
  return !FORBIDDEN_PATTERNS.some(p => p.re.test(text))
}

// 통과 실패 시 첫 위반 사유(로깅용).
export function firstViolation(text: string): string | null {
  for (const p of FORBIDDEN_PATTERNS) if (p.re.test(text)) return p.name
  return null
}
